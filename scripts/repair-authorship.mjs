import fs from 'node:fs';
import path from 'node:path';
import matter from 'gray-matter';
import { isAutoGeneratedArticle } from './lib/provenance.mjs';

const ARTDIR = path.join(process.cwd(), 'src', 'content', 'articles');
const files = fs.readdirSync(ARTDIR).filter((f) => f.endsWith('.md'));

const REPLACEMENTS = [
  { regex: /\bmy work on\b/gi, replacement: "the research in" },
  { regex: /\bI (?:previously )?proposed\b/gi, replacement: "the author proposed" },
  { regex: /\bmy paper\b/gi, replacement: "the paper" },
  { regex: /\bwe propose\b/gi, replacement: "the authors propose" }
];

let repaired = 0;

for (const f of files) {
  const filePath = path.join(ARTDIR, f);
  const text = fs.readFileSync(filePath, 'utf8');
  let fm = null;

  try {
    fm = matter(text);
  } catch (err) {
    continue;
  }

  const data = fm.data;
  const title = String(data.title ?? '');
  const isAutoGenerated = isAutoGeneratedArticle({ type: data.type, title, date: data.date });

  if (isAutoGenerated) {
    let modifiedText = text;
    let changed = false;

    for (const { regex, replacement } of REPLACEMENTS) {
      if (regex.test(modifiedText)) {
        modifiedText = modifiedText.replace(regex, replacement);
        changed = true;
      }
    }

    if (changed) {
      fs.writeFileSync(filePath, modifiedText, 'utf8');
      repaired++;
      console.log(`Repaired authorship phrasing in ${f}`);
    }
  }
}

if (repaired > 0) {
  console.log(`Successfully repaired authorship phrasing in ${repaired} auto-generated article(s).`);
} else {
  console.log(`No auto-generated authorship repair needed.`);
}
